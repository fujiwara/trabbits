func (p *Proxy) replyQueueDeclare(_ context.Context, f *amqp091.MethodFrame, m *amqp091.QueueDeclare) error {
	id := f.Channel()
	chs, err := p.GetChannels(id)
	if err != nil {
		return err
	}

	p.probeLog("c->t Queue.Declare", "message", m)
	if m.Queue == "" {
		m.Queue = AutoGenerateQueueNamePrefix + generateID()
		p.probeLog("c->t Queue.Declare auto-generated name", "name", m.Queue)
	}
	var messages, consumers int
	var queueName string
	for i, ch := range chs {
		us := p.Upstream(i)
		p.probeLog("t->u Queue.Declare", "upstream", us.String(), "message", m)
		q, err := us.QueueDeclareWithTryPassive(ch, m)
		if err != nil {
			return fmt.Errorf("failed to declare queue on upstream %s: %w", us.String(), err)
		}
		p.probeLog("t<-u Queue.Declare result", "upstream", us.String(), "result", q)
		messages += q.Messages
		consumers += q.Consumers
		if queueName == "" {
			queueName = q.Name
		} else if queueName != q.Name {
			return NewError(amqp091.InternalError, "queue name mismatch")
		}
		us.CleanupAutoGeneratedQueue(q.Name)
	}
	p.probeLog("c<-t Queue.Declare completed", "final_name", queueName, "total_messages", messages, "total_consumers", consumers)

	return p.send(id, &amqp091.QueueDeclareOk{
		Queue:         queueName,
		MessageCount:  uint32(messages),
		ConsumerCount: uint32(consumers),
	})
}

